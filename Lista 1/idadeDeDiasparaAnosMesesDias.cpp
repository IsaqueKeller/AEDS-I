#include <iostream>
#include <math.h>
using namespace std;

void idadeEmDias (int totaldedias) {
	int ano = totaldedias/365;
	int resto = totaldedias%365; 
	
	int mes = resto/30; 	
	
	int dias = resto%30;
	
	cout << ano << " anos " << mes << " meses " << dias << " dias" << endl;
	
}

int main() {
int totaldedias;
	
	cout << "Informe o seu valor em dias para ser transformado em anos e meses: " << endl;
	cin >> totaldedias; 
	
   idadeEmDias(totaldedias);
   return 0; 
}
