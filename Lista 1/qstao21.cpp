// Faça um programa que receba um numero e informe se ele é par ou impar.
#include <iostream>
using namespace std;

void funcaogeral (int numero) {
	if (numero % 2 == 0) {
		cout << "O numero eh par" << endl;
	}
	else {
		cout << "O numero eh impar" << endl;
	}
}

int main (void) {
	int a = 0;
	
	cout << "Informe um numero: " << endl;
	cin >> a;
	
	funcaogeral(a);
}
