#include <iostream>
#include <math.h>
using namespace std;

float funcaoGeral (float ano , float mes , float dias) {
	
	ano = 365*ano;
	mes = 30* mes;
	dias = 1* dias;
	
	return ano + mes + dias;
}


int main () {
float ano = 0 , mes = 0 , dias = 0;
	
	cout << "Informe quantos anos: " << endl;
	cin >> ano;
	
	cout << "Informe quantos meses: " << endl;
	cin >> mes;
	
	cout << "Informe quantos dias: " << endl;
	cin >> dias;
	
	cout << "Sua idade e de: " << funcaoGeral(ano , mes , dias) << " dias" << endl;
}
