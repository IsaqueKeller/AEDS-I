#include <iostream>
using namespace std;

void age_function (int age) {
  
	if (age <= 7) {
		cout << age << " - Infant A" << endl;
	}
	else if (age <= 10) {
		cout << age << " - Infant B" << endl; 
	}
	else if (age <= 13) {
		cout << age << " - Juvenile A" << endl;
	}
	else if (age <= 17) {
		cout << age << " - Juvenile B" << endl;
	}
	else  {
		cout << age << " - Adult" << endl;
	}
}

int main () {
	int a = 0;

	cout << "Type in your age: " << endl;
	cin >> a;

	while (a < 5) {
		cout << "Invalid age." << endl;
	cin >> a;
	}
	age_function (a);
}
