#include <iostream>
using namespace std;

float calculus (float base , float exponent) {
	float result = 1;
	int i = 1;
	while (i <= exponent) {
		result *= base;
		i ++;
	}
	cout << "The result is: " << result << endl;
}

int main () {
	float base , exponent = 0;
	cout << "Inform a base: " << endl;
	cin >> base;
	
	cout << "Inform an exponent: " << endl;
	cin >> exponent;
	
	calculus (base , exponent);
}
